package com.smarsh.ccm.utils;

public class EmployeesGetSet {

	String id;
	String assDevID;
	String name;
	String email;
	String department;
	String regStatus;
	String netGuardDev;
	String verizonSet;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAssDevID() {
		return assDevID;
	}
	public void setAssDevID(String assDevID) {
		this.assDevID = assDevID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getRegStatus() {
		return regStatus;
	}
	public void setRegStatus(String regStatus) {
		this.regStatus = regStatus;
	}
	public String getNetGuardDev() {
		return netGuardDev;
	}
	public void setNetGuardDev(String netGuardDev) {
		this.netGuardDev = netGuardDev;
	}
	public String getVerizonSet() {
		return verizonSet;
	}
	public void setVerizonSet(String verizonSet) {
		this.verizonSet = verizonSet;
	}

		
}
